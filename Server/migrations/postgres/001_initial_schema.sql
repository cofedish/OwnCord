CREATE TABLE IF NOT EXISTS voice_states (
    user_id      BIGINT PRIMARY KEY REFERENCES users(id) ON DELETE CASCADE,
    channel_id   BIGINT  NOT NULL REFERENCES channels(id) ON DELETE CASCADE,
    muted        INTEGER NOT NULL DEFAULT 0,
    deafened     INTEGER NOT NULL DEFAULT 0,
    speaking     INTEGER NOT NULL DEFAULT 0,
    joined_at    TEXT    NOT NULL DEFAULT (to_char(timezone('utc', now()), 'YYYY-MM-DD"T"HH24:MI:SS"Z"')),
    camera       INTEGER NOT NULL DEFAULT 0,
    screenshare  INTEGER NOT NULL DEFAULT 0
);

CREATE INDEX IF NOT EXISTS idx_voice_states_channel ON voice_states(channel_id);

CREATE TABLE IF NOT EXISTS dm_participants (
    channel_id BIGINT NOT NULL REFERENCES channels(id) ON DELETE CASCADE,
    user_id    BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    PRIMARY KEY (channel_id, user_id)
);

CREATE INDEX IF NOT EXISTS idx_dm_participants_user ON dm_participants(user_id);

CREATE TABLE IF NOT EXISTS dm_open_state (
    user_id    BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    channel_id BIGINT NOT NULL REFERENCES channels(id) ON DELETE CASCADE,
    opened_at  TEXT   NOT NULL DEFAULT (to_char(timezone('utc', now()), 'YYYY-MM-DD"T"HH24:MI:SS"Z"')),
    PRIMARY KEY (user_id, channel_id)
);

CREATE TABLE IF NOT EXISTS rate_lockouts (
